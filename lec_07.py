# 10. АНАЛИЗ ДАТАСЕТА TITANIC 
# ==============================================================================

file_titanic = "data/titanic.csv"
if not os.path.exists(file_titanic):
    print(f"\nФайл {file_titanic} не найден. Пропускаем этот раздел.")
    print("Чтобы выполнить его, поместите titanic.csv в папку 'data'.")
else:
    titanic = pd.read_csv(file_titanic)
    print("\nПервые 5 строк Titanic:")
    print(titanic.head())

    # --------------------------------------------------------------------------
    # 10.1. Доля выживших по полу
    # --------------------------------------------------------------------------
    # Группируем по полу и считаем среднее значение столбца Survived.
    # Поскольку Survived = 1 (выжил) или 0 (не выжил), среднее даёт долю выживших.
    survival_sex = titanic.groupby("Sex")["Survived"].mean()
    print("\nДоля выживших по полу:")
    print(survival_sex)

    # --------------------------------------------------------------------------
    # 10.2. Доля выживших по полу и классу (Pclass)
    # --------------------------------------------------------------------------
    # Группируем по двум признакам
    survival_sex_class = titanic.groupby(["Sex", "Pclass"])["Survived"].mean()
    print("\nДоля выживших по полу и классу (до unstack):")
    print(survival_sex_class)

    # --------------------------------------------------------------------------
    # 10.3. Превращаем второй уровень индекса (Pclass) в столбцы для удобства
    # --------------------------------------------------------------------------
    survival_pivot = survival_sex_class.unstack()
    print("\nТа же информация после unstack (пол в строках, класс в столбцах):")
    print(survival_pivot)

    # --------------------------------------------------------------------------
    # 10.4. Визуализация
    # --------------------------------------------------------------------------
    survival_pivot.plot(kind='bar', figsize=(8,6))
    plt.title('Доля выживших по полу и классу')
    plt.ylabel('Доля выживших')
    plt.xticks(rotation=0)
    plt.grid(axis='y', linestyle='--', alpha=0.7)
    plt.legend(title='Класс')
    plt.tight_layout()
    plt.show()