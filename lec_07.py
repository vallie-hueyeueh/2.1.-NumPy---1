# ==============================================================================
# 1. ИМПОРТ БИБЛИОТЕК
# ==============================================================================
import pandas as pd            # работа с таблицами и временными рядами
import numpy as np             # математические функции, генерация случайных чисел
import matplotlib.pyplot as plt # построение графиков
from datetime import datetime  # работа с датами
import os                      # проверка существования файлов

# Настройка отображения (чтобы было видно все столбцы)
pd.set_option('display.max_columns', None)
pd.set_option('display.width', 1000)

# ==============================================================================
# 2. МНОГОУРОВНЕВЫЕ ИНДЕКСЫ (MultiIndex)
# ==============================================================================
# 2.1. Создание Series с MultiIndex из словаря, где ключи – кортежи
# ------------------------------------------------------------------------------
# Ключи словаря – кортежи (product, year, count). Pandas автоматически создаст
# иерархический индекс из трёх уровней.
data_dict = {
    ("A1", 2025, 1): 11, ("A1", 2025, 2): 12,
    ("A1", 2026, 1): 13, ("A1", 2026, 2): 14,
    ("A1", 2027, 1): 15, ("A1", 2027, 2): 16,
    ("A2", 2025, 1): 11, ("A2", 2025, 2): 12,
    ("A2", 2026, 1): 13, ("A2", 2026, 2): 14,
    ("A2", 2027, 1): 15, ("A2", 2027, 2): 16,
    ("A3", 2025, 1): 21, ("A3", 2025, 2): 22,
    ("A3", 2026, 1): 23, ("A3", 2026, 2): 24,
    ("A3", 2027, 1): 25, ("A3", 2027, 2): 26,
}

sr = pd.Series(data_dict)

# Дадим имена каждому уровню индекса – это упрощает понимание и дальнейшую работу.
sr.index.names = ["product", "year", "count"]

print("\n--- Series с трёхуровневым индексом ---")
print(sr)
print("\nТип индекса:", type(sr.index))  # pandas.core.indexes.multi.MultiIndex

# ------------------------------------------------------------------------------
# 2.2. Метод unstack – превращает один уровень индекса в столбцы
# ------------------------------------------------------------------------------
# level=0 означает, что первый уровень (product) станет названиями столбцов.
# Получится таблица, где строки – комбинации (year, count), а столбцы – product.
print("\n--- unstack(level=0): product → columns ---")
print(sr.unstack(level=0))

# Можно также unstack несколько уровней, но это реже нужно.

# ------------------------------------------------------------------------------
# 2.3. Создание DataFrame с MultiIndex и в строках, и в столбцах
# ------------------------------------------------------------------------------
# Зафиксируем генератор случайных чисел, чтобы результаты были повторяемы.
rng = np.random.default_rng(42)

# Индекс строк: комбинации property (A1, A2) и year (2025, 2026)
mi_rows = pd.MultiIndex.from_product([["A1", "A2"], [2025, 2026]],
                                      names=["property", "year"])

# Индекс столбцов: комбинации shop (B1, B2) и month (jan, feb)
mi_cols = pd.MultiIndex.from_product([["B1", "B2"], ["jan", "feb"]],
                                      names=["shop", "month"])

# Создаём DataFrame 4x4 со случайными данными
df_multi = pd.DataFrame(rng.random((4, 4)), index=mi_rows, columns=mi_cols)

print("\n--- DataFrame с иерархическими индексами строк и столбцов ---")
print(df_multi)

# ------------------------------------------------------------------------------
# 2.4. Различные способы доступа к данным в таком DataFrame
# ------------------------------------------------------------------------------
# Обращение по первому уровню столбцов (возвращает DataFrame с одним уровнем)
print("\n--- Выбор столбца первого уровня 'B2' ---")
print(df_multi["B2"])

# Обращение по двум уровням столбцов (возвращает Series)
print("\n--- Выбор конкретного столбца ('B2', 'feb') ---")
print(df_multi[("B2", "feb")])

# Срез строк по позициям (iloc – работает по номерам строк, не по меткам)
print("\n--- Срез строк iloc[1:3] (строки 1 и 2) ---")
print(df_multi.iloc[1:3])

# ==============================================================================
# 3. ПРОДВИНУТАЯ ИНДЕКСАЦИЯ С pd.IndexSlice
# ==============================================================================
# IndexSlice позволяет делать срезы сразу по нескольким уровням иерархического индекса.
# Это удобно, когда нужно выбрать, например, все строки с year = 2025 и все столбцы с month = 'jan'.

# Создаём объект IndexSlice
idx = pd.IndexSlice

# Пример 1: все строки, у которых второй уровень (year) = 2025,
# и все столбцы, у которых второй уровень (month) = 'jan'
selection1 = df_multi.loc[idx[:, 2025], idx[:, 'jan']]
print("\n--- Строки с year=2025, столбцы с month='jan' ---")
print(selection1)

# Пример 2: только строки с property='A1' (первый уровень строк),
# и только столбцы с shop='B1' (первый уровень столбцов)
selection2 = df_multi.loc[idx['A1', :], idx['B1', :]]
print("\n--- Строки с property='A1', столбцы с shop='B1' ---")
print(selection2)

# IndexSlice особенно полезен, когда нужно передать сложный срез в .loc

# ==============================================================================
# 4. РАБОТА С РЕАЛЬНЫМ ДАТАСЕТОМ: РОЖДАЕМОСТЬ В США (births.csv)
# ==============================================================================
# ------------------------------------------------------------------------------
# 4.1. Проверка наличия файла и загрузка
# ------------------------------------------------------------------------------
file_births = "data/births.csv"   # путь относительно текущей папки
if not os.path.exists(file_births):
    print(f"\nФайл {file_births} не найден. Пропускаем этот раздел.")
    print("Чтобы выполнить его, поместите births.csv в папку 'data' рядом со скриптом.")
    births = None
else:
    births = pd.read_csv(file_births)
    print("\nПервые 5 строк загруженного датасета:")
    print(births.head())

    print("\nИнформация о типах столбцов до преобразований:")
    print(births.dtypes)

    # --------------------------------------------------------------------------
    # 4.2. Очистка данных: приведение year, month, day к целому типу
    # --------------------------------------------------------------------------
    # В исходных данных эти столбцы могут быть типа float из-за возможных пропусков.
    # Для создания даты нужны целые числа. Если бы были пропуски, их нужно было бы
    # обработать отдельно (например, удалить или заполнить). В данном наборе их нет.
    births['year'] = births['year'].astype(int)
    births['month'] = births['month'].astype(int)
    births['day'] = births['day'].astype(int)

    print("\nПосле приведения к int (первые 5 строк):")
    print(births[['year','month','day']].head())

    # --------------------------------------------------------------------------
    # 4.3. Добавление столбца "десятилетие" (decade)
    # --------------------------------------------------------------------------
    # Десятилетие = год, округлённый вниз до ближайшего десятка.
    # Например, 1969 → 1960, 1975 → 1970.
    births['decade'] = (births['year'] // 10) * 10
    print("\nСтолбец 'decade' добавлен (первые 5 строк):")
    print(births[['year','decade']].head())

    # --------------------------------------------------------------------------
    # 4.4. Создание полноценной даты и установка её в качестве индекса
    # --------------------------------------------------------------------------
    # Лучший способ – передать в pd.to_datetime сразу три колонки.
    # Это автоматически создаст datetime-объекты.
    births['date'] = pd.to_datetime(births[['year', 'month', 'day']])
    births = births.set_index('date')
    print("\nИндекс заменён на дату (первые 10 значений):")
    print(births.index[:10])

    # --------------------------------------------------------------------------
    # 4.5. Добавление дня недели (0 = понедельник, 6 = воскресенье)
    # --------------------------------------------------------------------------
    births['dayofweek'] = births.index.dayofweek
    print("\nДобавлен столбец 'dayofweek' (первые 5 строк):")
    print(births[['births','gender','dayofweek']].head())

    # ==========================================================================
    # 5. СВОДНЫЕ ТАБЛИЦЫ (pivot_table)
    # ==========================================================================
    # --------------------------------------------------------------------------
    # 5.1. Среднее число рождений по дням недели для каждого десятилетия
    # --------------------------------------------------------------------------
    births_dow = births.pivot_table(
        values='births',      # что агрегируем
        index='dayofweek',    # строки будущей таблицы
        columns='decade',     # столбцы будущей таблицы
        aggfunc='mean'        # функция агрегации (среднее)
    )
    print("\n--- Среднее число рождений по дням недели (по десятилетиям) ---")
    print(births_dow)

    # --------------------------------------------------------------------------
    # 5.2. Суммарное число рождений по годам и полу
    # --------------------------------------------------------------------------
    births_years = births.pivot_table(
        values='births',
        index='year',
        columns='gender',
        aggfunc='sum'
    )
    print("\n--- Сумма рождений по годам и полу (первые 5 строк) ---")
    print(births_years.head())

    # --------------------------------------------------------------------------
    # 5.3. Среднее число рождений для каждого дня месяца (без учёта года)
    # --------------------------------------------------------------------------
    births_dom = births.pivot_table(
        values='births',
        index=['month', 'day'],   # группируем по двум колонкам – получаем MultiIndex
        aggfunc='mean'
    )
    print("\n--- Среднее по дням месяца (первые 10 строк) ---")
    print(births_dom.head(10))

    # ==========================================================================
    # 6. ПОДГОТОВКА ДАННЫХ ДЛЯ ГРАФИКА ВНУТРИГОДОВОЙ ДИНАМИКИ
    # ==========================================================================
    # У births_dom индекс – MultiIndex (month, day). Чтобы построить график по дням года,
    # нужно превратить этот индекс в обычные колонки, а затем создать единую дату
    # в пределах одного года (например, 2012 – невисокосный).

    # Сбрасываем индекс: теперь month и day – обычные колонки
    births_dom_reset = births_dom.reset_index()
    # Убедимся, что они целые (хотя после reset_index они обычно уже целые)
    births_dom_reset['month'] = births_dom_reset['month'].astype(int)
    births_dom_reset['day'] = births_dom_reset['day'].astype(int)

    # Создаём список дат для 2012 года, используя zip для безопасного перебора пар
    dates_2012 = [datetime(2012, m, d) for m, d in zip(births_dom_reset['month'],
                                                        births_dom_reset['day'])]
    # Строим Series с этими датами в качестве индекса
    births_dom_2012 = pd.Series(births_dom_reset['births'].values, index=dates_2012)

    print("\n--- Средние рождения по дням месяца, привязанные к 2012 году (первые 5) ---")
    print(births_dom_2012.head())

    # ==========================================================================
    # 7. ВИЗУАЛИЗАЦИЯ
    # ==========================================================================

    plt.figure(figsize=(14, 6))

    # График 1: динамика по дням недели для разных десятилетий
    plt.subplot(1, 2, 1)
    births_dow.plot(ax=plt.gca(), marker='o', linewidth=2)
    plt.title('Среднее число рождений по дням недели', fontsize=14)
    plt.xlabel('День недели (0=пн, 6=вс)')
    plt.ylabel('Среднее число рождений')
    plt.grid(True, linestyle='--', alpha=0.6)
    plt.legend(title='Десятилетие')

    # График 2: внутригодовой профиль (по дням месяца)
    plt.subplot(1, 2, 2)
    births_dom_2012.plot(ax=plt.gca(), color='green', linewidth=1.5)
    plt.title('Среднее число рождений по дням месяца (2012)', fontsize=14)
    plt.xlabel('День года')
    plt.ylabel('Среднее число рождений')
    plt.grid(True, linestyle='--', alpha=0.6)

    plt.tight_layout()
    plt.show()

    # ==========================================================================
    # 8. ДОПОЛНИТЕЛЬНЫЙ ПРИМЕР: ГИСТОГРАММА ДЛЯ 1969 ГОДА
    # ==========================================================================

    births_1969 = births[births.index.year == 1969]['births']
    if not births_1969.empty:
        plt.figure(figsize=(12,5))
        plt.subplot(1,2,1)
        births_1969.hist(bins=100, edgecolor='black', alpha=0.7)
        plt.title('Гистограмма рождений в 1969 году')
        plt.xlabel('Число рождений')
        plt.ylabel('Частота')
        plt.grid(True, linestyle='--', alpha=0.5)

        plt.subplot(1,2,2)
        births['births'].hist(bins=100, edgecolor='black', alpha=0.7, color='green')
        plt.title('Гистограмма всех рождений')
        plt.xlabel('Число рождений')
        plt.ylabel('Частота')
        plt.grid(True, linestyle='--', alpha=0.5)
        plt.tight_layout()
        plt.show()
    else:
        print("\nНет данных за 1969 год.")

    # ==========================================================================
    # 9. КВАРТИЛИ РАСПРЕДЕЛЕНИЯ (процентили)
    # ==========================================================================
    q = np.percentile(births['births'], [25, 50, 75])
    print("\n--- Квартили числа рождений ---")
    print(f"25% (Q1) = {q[0]:.1f}")
    print(f"50% (медиана) = {q[1]:.1f}")
    print(f"75% (Q3) = {q[2]:.1f}")

