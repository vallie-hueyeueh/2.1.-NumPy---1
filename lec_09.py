import os                              # Для проверки существования файла
import numpy as np                     # Для математических операций и логики
import pandas as pd                    # Для работы с таблицами данных
import matplotlib.pyplot as plt        # Для отрисовки графиков

# Устанавливаем классический стиль графиков
plt.style.use('classic')

# ШАГ 2: БЕЗОПАСНАЯ ЗАГРУЗКА ДАННЫХ
# Указываем путь к файлу. 
# файл FremontBridge.csv должен лежать по этому пути
file_path = "./digital_python-25-26/data/FremontBridge.csv"

# Проверяем, существует ли файл, чтобы программа не вылетела с непонятной ошибкой
if not os.path.exists(file_path):
    print(f"ОШИБКА: Файл не найден по пути: {file_path}")
    print("папка 'digital_python-25-26/data/' существует рядом - скриптом, и файл 'FremontBridge.csv' лежит внутри.")
    exit() # Останавливаем программу, если файла нет

print("Файл найден, начинаем обработку данных")

# Читаем таблицу с данными
df = pd.read_csv(
    file_path,
    index_col="Date",                                # Колонка с датой становится индексом (осью времени)
    parse_dates=True,                                # Авто-распознавание дат
    date_format="%m/%d/%Y %I:%M:%S %p"               # Формат: Месяц/День/Год Часы:Минуты:Секунды AM/PM
)

# ШАГ 3: ОЧИСТКА И ПОДГОТОВКА ДАННЫХ
# Переименовываем длинные названия колонок в короткие и удобные
df.columns = ["Total", "East", "West"]
# Удаляем пустые строки, где датчики сломались или не записали данные (чтобы не было ошибок в расчетах)
df = df.dropna()
print("Данные успешно очищены. Строим графики")

# ШАГ 4: АНАЛИЗ И ПОСТРОЕНИЕ ГРАФИКОВ

# --- ГРАФИК 1: Общая динамика по неделям ---
# Берем данные, группируем их по неделям ("W") и суммируем (.sum())
weekly = df.resample("W").sum()
# Рисуем график с разными стилями линий для Total, East и West
weekly.plot(style=["-", ":", "--"], figsize=(10, 5), title="Суммарный трафик по неделям")
plt.ylabel("Кол-во велосипедистов")
plt.tight_layout() # Чтобы надписи не обрезались
plt.show() # Показываем первый график (нужно будет закрыть окно, чтобы код пошел дальше)

# --- ГРАФИК 2: Сглаженный тренд (Скользящее окно) ---
# Группируем по дням ("D"), суммируем, а затем берем "скользящее среднее" за 30 дней
# Это убирает резкие скачки и показывает плавный тренд (например, сезонность - зимой меньше, летом больше)
daily = df.resample("D").sum()
daily.rolling(30, center=True, win_type="gaussian").mean(std=5).plot(style=["-", ":", "--"], figsize=(10, 5), title="Сглаженный тренд (30 дней)")
plt.ylabel("Среднемесячное кол-во велосипедистов")
plt.tight_layout()
plt.show() # Показываем второй график

# --- ГРАФИК 3: Почасовая активность (Будни vs Выходные) ---
# Создаем массив, где каждому дню присваивается ярлык "Будни" или "Выходные"
# weekday < 5 означает дни с понедельника (0) по пятницу (4)
w1 = np.where(df.index.weekday < 5, "Будни", "Выходные")

# Группируем данные сначала по типу дня (w1), а затем по времени суток
t1 = df.groupby([w1, df.index.time]).mean()

# Создаем большое полотно для двух параллельных графиков
fig, ax = plt.subplots(1, 2, figsize=(14, 5))

# Левый график (Будни)
ax[0].set_ylim(0, 600) # Лимит оси Y от 0 до 600
t1.loc["Будни"].plot(ax=ax[0], title="Средний трафик в БУДНИЕ дни")
ax[0].set_ylabel("Кол-во велосипедистов (в час)")
ax[0].set_xlabel("Время суток")

# Правый график (Выходные)
ax[1].set_ylim(0, 600) # Лимит оси Y такой же, чтобы корректно сравнивать глазами
t1.loc["Выходные"].plot(ax=ax[1], title="Средний трафик в ВЫХОДНЫЕ дни")
ax[1].set_xlabel("Время суток")

plt.tight_layout()

# Сохраняем финальный результат в картинку
fig.savefig('ttt.png')
print("Финальный график сохранен в файл 'ttt.png'")

# Показываем финальный график
plt.show()
print("Анализ успешно завершен")