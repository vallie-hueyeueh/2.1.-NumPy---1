print("a_simple[3] =", a_simple[3])           # элемент с индексом 3 (четвёртый)

# Срез – возвращает массив (даже если длина среза равна 1)
print("a_simple[3:4] =", a_simple[3:4])       # массив из одного элемента [3]

# Индексация булевой маской – выбираем все элементы, равные 3
mask_3 = (a_simple == 3)
print("a_simple == 3 ->", mask_3)
print("a_simple[a_simple == 3] =", a_simple[mask_3])   # [3]

# Индексация списком индексов (fancy indexing) – тоже массив
print("a_simple[ [3,5,0] ] =", a_simple[[3,5,0]])

