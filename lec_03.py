# 6. ЛОГИЧЕСКИЕ ОПЕРАТОРЫ AND/OR VS. ПОБИТОВЫЕ & |
# =============================================================================
print("\n" + "="*60)
print("6. ОТЛИЧИЕ and / or ОТ & / |")
print("="*60)

# Операторы and, or, not предназначены для работы с отдельными булевыми значениями
# (или объектами, которые можно привести к bool). Они возвращают одно из значений,
# а не булеву маску.

print("bool(42) =", bool(42), "   bool(0) =", bool(0))
print("42 and 0 =", 42 and 0, " -> bool =", bool(42 and 0))   # 0 -> False
print("42 or 0  =", 42 or 0,  " -> bool =", bool(42 or 0))    # 42 -> True

# Для поэлементных логических операций над массивами используют & (И), | (ИЛИ), ~ (НЕ).
# Они работают побитово, но с булевыми массивами дают нужный результат.

# Демонстрация на числах (побитовые операции):
print("\nПобитовые операции над целыми числами:")
print("bin(42) =", bin(42))   # 0b101010
print("bin(59) =", bin(59))   # 0b111011
print("42 & 59 =", bin(42 & 59))   # 0b101010 (42)
print("42 | 59 =", bin(42 | 59))   # 0b111011 (59)

# А теперь логические and/or для тех же чисел (не побитовые!):
print("\nЛогические and/or для чисел (не побитовые):")
print("42 and 59 =", bin(42 and 59))   # возвращает 59 (последнее истинное значение)
print("42 or 59  =", bin(42 or 59))    # возвращает 42 (первое истинное)

# Вывод: при работе с булевыми массивами всегда используйте &, |, ~.

# =============================================================================
# 7. ПРИМЕНЕНИЕ BROADCASTING: ПОСТРОЕНИЕ СЕТКИ И ВИЗУАЛИЗАЦИЯ
# =============================================================================
print("\n" + "="*60)
print("7. ПОСТРОЕНИЕ ДВУМЕРНОЙ СЕТКИ И ВИЗУАЛИЗАЦИЯ ФУНКЦИИ")
print("="*60)

# Часто требуется вычислить функцию двух переменных на прямоугольной сетке.
# Broadcasting позволяет легко создать такую сетку из двух одномерных массивов координат.

# Создаём координаты x и y от 0 до 10, по 100 точек в каждом направлении.
x = np.linspace(0, 10, 100)   # 100 точек равномерно от 0 до 10
y = np.linspace(0, 10, 100)

# Чтобы получить сетку, превратим y в столбец (добавим ось)
y_col = y[:, np.newaxis]       # форма (100, 1)
print("x.shape =", x.shape)           # (100,)
print("y_col.shape =", y_col.shape)   # (100, 1)

# Теперь вычислим функцию z = sin(x)*y + cos(10 + y*x)^3.
# Благодаря broadcasting:
#   - x (100,) будет интерпретирован как (1,100) и растянется по строкам до (100,100)
#   - y_col (100,1) растянется по столбцам до (100,100)
#   - операции выполняются поэлементно, результат – матрица 100x100.
z = np.sin(x) * y_col + np.cos(10 + y_col * x) ** 3
print("z.shape =", z.shape)   # (100, 100)

# Визуализация с помощью matplotlib
plt.figure(figsize=(8, 6))
# imshow показывает матрицу как изображение. extent задаёт границы осей.
# origin='lower' помещает начало координат в левый нижний угол.
plt.imshow(z, extent=[0, 10, 0, 10], origin='lower', aspect='auto', cmap='viridis')
plt.colorbar(label='z')                # цветовая шкала
plt.xlabel('x')
plt.ylabel('y')
plt.title('z = sin(x)*y + cos(10 + y*x)**3')
plt.show()

